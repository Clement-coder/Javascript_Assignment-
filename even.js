// 2. Write a program that checks if a number is even or odd.

let number = 2;
let name = "Clement";
let lastName = "Raymond";
let fullName = name + " " + lastName;

if (number % 2 === 0) {
    console.log(fullName + " says the number is even.");
} else {
    console.log(fullName + " says the number is odd.");
}
